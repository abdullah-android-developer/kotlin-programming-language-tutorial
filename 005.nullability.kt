/*
 	What is null DataType?
 		- null DataType means that a variable contains no value, its null.
 */

// Making a null variable
fun main() {
	// We make null variables by putting a ? (question) mark after the DataType.
	var name: String? = null;
	println(name)
	name = "Abdullah Sajjad"
	print(name)
}