fun main() {
	// Made array with the use of arrayOf() function
	var names = arrayOf("Muhammad", "Abdullah", "Ali", "Abu Bakar", "Usman", "Umer")
	println(names[0]) // It will print the Muhammad
	println(names[2]) // It will print the Ali
	/*
 	Output:-
 		Muhammad
 		Ali
 */
}