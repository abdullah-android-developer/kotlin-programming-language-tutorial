// Writing Hello World Code In Kotlin:-

// 1. fun is the keyword to make functions.
// 2. main() is the function from which our code starts executing.
fun main() {
	// 3. print() is the function which print and give us a output without a new line at end.
	print("hello world, ")
	// 4. println() is the function which print and give us a output with a new line at a end.
	println("Abdullah")
	print("Sajjad")
}
/* 5. Output:-
			hello world, Abdullah
            Sajjad
 */